from datetime import date


# 固定イベント: {(月, 日): イベント名}
_FIXED_EVENTS: dict[tuple[int, int], str] = {
    (1, 1):   "元日・お正月",
    (1, 7):   "七草粥（人日の節句）",
    (1, 14):  "成人の日前後",
    (2, 3):   "節分",
    (2, 14):  "バレンタインデー",
    (3, 3):   "ひな祭り",
    (3, 14):  "ホワイトデー",
    (4, 1):   "エイプリルフール",
    (5, 3):   "憲法記念日",
    (5, 4):   "みどりの日",
    (5, 5):   "こどもの日・端午の節句",
    (6, 21):  "夏至",
    (7, 7):   "七夕",
    (8, 6):   "広島原爆の日",
    (8, 9):   "長崎原爆の日",
    (8, 15):  "終戦記念日・お盆",
    (9, 9):   "重陽の節句",
    (9, 21):  "秋分の日前後",
    (10, 31): "ハロウィン",
    (11, 3):  "文化の日",
    (11, 11): "ポッキーの日",
    (11, 15): "七五三",
    (12, 22): "冬至",
    (12, 24): "クリスマスイブ",
    (12, 25): "クリスマス",
    (12, 31): "大晦日",
}


def get_event_on_date(d: date) -> str | None:
    """指定日に該当するイベント名を返す。なければ None。"""
    return _FIXED_EVENTS.get((d.month, d.day))
